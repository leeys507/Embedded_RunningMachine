#define MATRIX_FILE_NAME "/dev/matrix_driver"

int main(void)
{
	int fd;
	int set_num;
	int i=0;

	double result;
	
	char num[11][5] = {
		0x81, 0xbd, 0xbd, 0xbd, 0x81,	//0
		0xf7, 0xf7, 0xf7, 0xf7, 0xf7,	//1
		0xc3, 0xfb, 0xc3, 0xdf, 0xc3,	//2
		0xc3, 0xfb, 0xc3, 0xfb, 0xc3,	//3
		0xb7, 0xb7, 0x81, 0xf7, 0xf7,	//4
		0x81, 0xbf, 0x81, 0xfd, 0x81,	//5
		0xbf, 0xbf, 0x81, 0xbd, 0x81,	//6
		0x81, 0xbd, 0xfd, 0xfd, 0xfd,	//7
		0x81, 0xbd, 0x81, 0xbd, 0x81,	//8
		0x81, 0xbd, 0x81, 0xfd, 0xfd,	//9
		0xa1, 0xad, 0xad, 0xad, 0xa1	//10
	};

	fd = open(MATRIX_FILE_NAME, O_RDWR);
	if(fd < 0)
	{
		fprintf(stderr, "Can't open %s\n", MATRIX_FILE_NAME);
		return -1;
	}


	while(1)
	{	
		for(i=0; i<11; i++)
		{
			write(fd, num[i], sizeof(char));
		}		
	
	}

		
	close(fd);
	return 0;

}


	
	
